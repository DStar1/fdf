#include "fdf.h"

void    alg()
{
    int x = 1;
    int y = 1;
    int x2 = 5;
    int y2 = 5;
    int dx = x2 - x;
    int dy = y2 - x;
    float m = dy / dx;
    int j = y;
    float e = m - 1;
    int i = 0;

    i = x1;
    while (i < x2)
    {
        pixel_put(i, j);
        if (e >= 0)
        {
            j++;
            e--;
        }
        i++;
        e+=m;
        i++;
    }
}